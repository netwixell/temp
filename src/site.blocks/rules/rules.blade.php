        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Участие во втором этапе Конкурса платное — 16 000 гривен со всей команды. В сумму оргвзноса входят билеты для мастеров на потоки «Парикхмахерство», «Визаж» и «Маникюр». А для модели проживание на Буковеле, завтраки и вечеринки</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Команды, прошедшие в финал, должны оплатить оргвзнос до 31 апреля 2019 года</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">На сцене команды-финалисты поэтапно воссоздадут конкурсные образы. Команда, которой удастся превзойти конкурентов, получит Гран-при в размере 100 000 гривен наличными и драгоценный переходящий Кубок Dream Team. Или награду в виде сертификатов по 25 000 гривен в одной из 4-х номинаций — «Лучший маникюр», «Лучшая причёска», «Лучший макияж» или «People’s Choice»</p>
        </li>
