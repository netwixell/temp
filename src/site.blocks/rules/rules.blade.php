        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">После звукового сигнала и команды «Стоп!» мастер должен сразу прекратить работу, убрать рабочее место и покинуть зону соревнований. Модели остаются на своих местах до завершения оценки этапа членами жюри</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
