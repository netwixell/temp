            <p class="rules__item"><b class="rules__title rules__title--b">Общие впечатления</b></p>
            <p class="rules__item rules__item--italic">Оценивается гармоничность целостного образа</p>
          </li>
        </ul>
        <div class="rules__requirements">
          <p class="rules__item rules__item--title rules__item--grey"><b class="rules__title rules__title--b">Общие требования:</b></p>
          <ul class="rules__list rules__list--grey">
            <li class="rules__item-wrapper">
              <p class="rules__item">Кожа рук модели ухоженная и здоровая</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Открытая, чистая линия кутикулы</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Длина ногтей от кутикулы до кончиков от 2 до 5 сантиметров</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Работа выполняется на смоделированных по базовой технологии ногтях</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Дизайн ногтей гармонирует с общим образом</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Ногти носибельные и отвечают тенденциям 2019 года</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Маникюр не содержит элементы в виде людей, животных, птиц, насекомых, одухотворенных персонажей и тд</p>
            </li>
          </ul>
          <p class="rules__item rules__item--title rules__item--grey"><b class="rules__title rules__title--b">Разрешается:</b></p>
          <ul class="rules__list rules__list--grey">
            <li class="rules__item-wrapper">
              <p class="rules__item">Использовать оборудование и инструменты любого типа</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Дополнять дизайн ногтей аксессуарами</p>
            </li>
            <li class="rules__item-wrapper">
              <p class="rules__item">Дизайн ногтей может быть выполнен в любой технике, иметь любую поверхность, например, плоскую или рельефную, содержать трехмерные (3D) элементы</p>
            </li>
          </ul>
