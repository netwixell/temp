        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Сфотографируйте прическу, маникюр, макияж и общий конкурсный образ, пользуясь представленными ниже критериями к фото</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Зарегистрируйте команду и оплатите оргвзнос в размере 3000 гривен до 1 апреля 2019 года (реквизиты для оплаты вы получите после регистрации). Вышлите пакет фотографий с выполненным заданием на почту <a href="mailto:dreamteam@molfarforum.com">dreamteam@molfarforum.com</a> с названием вашей команды в теме письма</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">С 5 по 14 апреля 2019 года профильное жюри определит работы 12 лучших команд, ещё 2 команды попадут в финальный этап Конкурса путем онлайн-голосования</p>
        </li>
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Результаты отборочного этапа будут опубликованы на сайте 17 апреля 2019 года</p>
        </li>
      </ol>
      <h3 class="rules__title rules__title--h3" id="Thefinalstage">Финальный этап</h3>
      <ol class="rules__list">
        <li class="rules__item-wrapper rules__item-wrapper--order">
