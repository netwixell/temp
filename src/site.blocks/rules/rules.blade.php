          <li class="rules__item-wrapper">
            <p class="rules__item"><b class="rules__title rules__title--b">Оригинальность образа</b></p>
            <p class="rules__item rules__item--italic">Оценивается неординарный подход и необычность видения</p>
          </li>
          <li class="rules__item-wrapper">
            <p class="rules__item"><b class="rules__title rules__title--b">Цветовое решение</b></p>
            <p class="rules__item rules__item--italic">Оценивается гармония и подбор цветов, их сочетание и баланс</p>
