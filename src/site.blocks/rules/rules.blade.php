      <ol class="rules__list">
        <li class="rules__item-wrapper rules__item-wrapper--order">
          <p class="rules__item">Для участия в конкурсе Dream Team соберите команду из четырех участников: парикмахера, визажиста, мастера маникюра и модели (возраст до 25 лет)</p>
        </li>
